    public void MostrarGrafo(){
        System.setProperty("org.graphstream.ui", "swing");
    // Crear un grafo simple
        Graph graph = new SingleGraph("Grafo de prueba");
        graph.addNode("A");
        graph.addNode("B");
        graph.addNode("C");
        graph.addEdge("AB", "A", "B");
        graph.addEdge("BC", "B", "C");
        graph.addEdge("CA", "C", "A");

        // Crear el SwingViewer para integrar con JPanel
        SwingViewer viewer = new SwingViewer(graph, Viewer.ThreadingModel.GRAPH_IN_GUI_THREAD);
        ViewPanel viewPanel = (ViewPanel) viewer.addDefaultView(false);  // No abrir en una nueva ventana

        // Establecer un tama√±o para el ViewPanel y agregarlo al JPanel principal
        viewPanel.setPreferredSize(new Dimension(400, 400));
        this.setLayout(new BorderLayout());
        this.add(viewPanel, BorderLayout.CENTER);
        this.revalidate(); // Para refrescar el panel y mostrar el grafo
        
    }
